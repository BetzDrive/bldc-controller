  uint32_t adc_value = 0;

  uint8_t adc_names[N_ADC_CHANNEL][64];
  strcpy(adc_names[0], "VSENSE_VIN");
  strcpy(adc_names[1], "VSENSE_A");
  strcpy(adc_names[2], "VSENSE_B");
  strcpy(adc_names[3], "VSENSE_C");
